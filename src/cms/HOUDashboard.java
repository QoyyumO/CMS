    //CSV escape method
    private String escapeCsv(String input) {
        if (input == null) {
            return "";
        }
        if (input.contains("\"") || input.contains(",") || input.contains("\n")) {
            return "\"" + input.replace("\"", "\"\"") + "\"";
        }
        return input;
    }

